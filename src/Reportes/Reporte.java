public void llamarReportes(String nombre)
     {
        try {
            conectarBD();
            //RUTA DEL ARCHIVO
            URL urlMaestro=getClass().getResource(nombre);
            //LLAMADO DEL ARCHIVO
            JasperReport reporte =(JasperReport) JRLoader.loadObject(urlMaestro);
            //LLENAMOS EL REPORTE
            JasperPrint jprint=JasperFillManager.fillReport(reporte, null,con);
            //VISTA DEL REPORTE
            JasperViewer view= new JasperViewer(jprint,false);
            //TITULO OPCIONAL
            view.setTitle("ABARROTERIA RIOS");
            //CIERRE DEL REPORTE
            view.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
            view.setVisible(true);
            con.close();
        } catch (JRException ex) {
            System.out.print(ex.getMessage());
        } catch (SQLException ex) {
           System.out.print(ex.getMessage());
        }
     }
     
     public void llamarReportePorfecha()
     {
       String f1 = JOptionPane.showInputDialog("INGRESE FECHA INICIAL (AAAA-MM-DD)");
String f2 = JOptionPane.showInputDialog("INGRESE FECHA FINAL (AAAA-MM-DD)");
if (f1 == null || f2 == null) return; 

fecha  = java.sql.Date.valueOf(f1);   
fecha2 = java.sql.Date.valueOf(f2);
 try {
            conectarBD();
            Map parametro = new HashMap();
            parametro.put("Fechai", fecha);
            parametro.put("Fechaf", fecha2);
            //RUTA DEL ARCHIVO
            URL urlMaestro=getClass().getResource("reportVentaporfecha.jasper");
            //LLAMADO DEL ARCHIVO
            JasperReport reporte =(JasperReport) JRLoader.loadObject(urlMaestro);
            //LLENAMOS EL REPORTE
            JasperPrint jprint=JasperFillManager.fillReport(reporte, parametro,con);
            //VISTA DEL REPORTE
            JasperViewer view= new JasperViewer(jprint,false);
            //TITULO OPCIONAL
            view.setTitle("JOYERIA AMBER");
            //CIERRE DEL REPORTE
            view.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
            view.setVisible(true);
            con.close();
        } catch (JRException ex) {
            System.out.print(ex.getMessage());
        } catch (SQLException ex) {
           System.out.print(ex.getMessage());
        }
     }



